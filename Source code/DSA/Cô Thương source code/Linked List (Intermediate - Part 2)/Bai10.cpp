void print_longest_increasing_sub_list(List &l)
{
    int ans = 1, maxLength = ans;
    Node *p = l.head->next, *q = l.head;
    while (p)
    {
        if (q->val > p->val)
        {
            if (maxLength < ans) maxLength = ans;
            ans = 1;
        }
        else ans++;
        q = p;
        p = p->next;
    }
    cout << maxLength;
}

int main()
{
    cin.tie(NULL);
    std::ios_base::sync_with_stdio(false);
    List l;

    list_initializing(l);
    int x;
    do{
        cin >> x;
        if (x != -1) add_tail(l, x);
        else break;
    } while(true);

    print_longest_increasing_sub_list(l);


    return 0;
}
